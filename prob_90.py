# Complete the fibonacciModified function below.
def fibonacciModified(t1, t2, n):
    fib = [t1, t2]
    while len(fib) != n:
        val = t1 + (t2 ** 2)
        fib.append(val)
        t1 = t2
        t2 = val


    return fib[n-1]


